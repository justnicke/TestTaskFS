import UIKit

extension UIFont {
    static func avenirNextMedium(_ size: CGFloat) -> UIFont? {
        return UIFont.init(name: "AvenirNext-Medium", size: size)
    }
    
    static func avenirNextBold(_ size: CGFloat) -> UIFont? {
        return UIFont.init(name: "AvenirNext-Bold", size: size)
    }
}
